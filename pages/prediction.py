"""
pages/prediction.py
---------------------
PAGE 4 - DIABETES PREDICTION

Professional prediction form with inputs: Age, Gender, BMI, Hypertension,
Heart Disease, Smoking History, Blood Glucose Level, HbA1c Level.

After submission: Prediction Result, High Risk Alert / Healthy Status,
Prediction Probability, Confidence Score, Risk Category.
"""

import streamlit as st

from utils.prediction import get_prediction_engine
from database.db_manager import add_health_record, add_prediction, get_best_model
from config import RISK_LABELS


def render():
    st.title("🩺 Diabetes Risk Prediction")
    st.caption("Fill in the patient's health and lifestyle information below to assess diabetes risk.")

    try:
        engine = get_prediction_engine()
    except FileNotFoundError as e:
        st.error(str(e))
        return

    st.info(f"Currently using model: **{engine.model_name}**")

    with st.form("prediction_form"):
        col1, col2 = st.columns(2)
        with col1:
            age = st.number_input("Age", min_value=1, max_value=120, value=40)
            gender = st.selectbox("Gender", ["Male", "Female", "Other"])
            bmi = st.number_input("BMI", min_value=10.0, max_value=70.0, value=25.0, step=0.1)
            hypertension = st.selectbox("Hypertension", ["No", "Yes"])
        with col2:
            heart_disease = st.selectbox("Heart Disease", ["No", "Yes"])
            smoking_history = st.selectbox(
                "Smoking History", ["never", "No Info", "former", "current", "not current", "ever"]
            )
            blood_glucose_level = st.number_input("Blood Glucose Level (mg/dL)", min_value=50.0, max_value=400.0, value=120.0, step=1.0)
            hba1c_level = st.number_input("HbA1c Level (%)", min_value=3.0, max_value=15.0, value=5.7, step=0.1)

        submitted = st.form_submit_button("🔍 Predict Diabetes Risk", type="primary", use_container_width=True)

    if submitted:
        patient_input = {
            "age": age,
            "gender": gender,
            "bmi": bmi,
            "hypertension": 1 if hypertension == "Yes" else 0,
            "heart_disease": 1 if heart_disease == "Yes" else 0,
            "smoking_history": smoking_history,
            "blood_glucose_level": blood_glucose_level,
            "HbA1c_level": hba1c_level,
        }

        result = engine.predict(patient_input)
        st.session_state["last_prediction"] = result
        st.session_state["last_patient_input"] = patient_input

        st.divider()
        st.subheader("📋 Prediction Result")

        if result["prediction_result"] == 1:
            st.error("🚨 **HIGH RISK ALERT** — The model predicts a likelihood of Diabetes.")
        else:
            st.success("✅ **HEALTHY STATUS** — The model predicts no immediate sign of Diabetes.")

        m1, m2, m3 = st.columns(3)
        m1.metric("Prediction Probability", f"{result['probability'] * 100:.1f}%")
        m2.metric("Confidence Score", f"{result['confidence_score'] * 100:.1f}%")
        m3.metric("Risk Category", result["risk_category"])

        risk_colors = {
            RISK_LABELS["low"]: "🟢",
            RISK_LABELS["moderate"]: "🟡",
            RISK_LABELS["high"]: "🔴",
        }
        st.markdown(f"### {risk_colors.get(result['risk_category'], '')} Risk Category: **{result['risk_category']}**")
        st.progress(min(result["probability"], 1.0))

        # --- Persist to database ---
        try:
            user_id = st.session_state["user_id"]
            record_id = add_health_record(user_id, patient_input)
            best_model = get_best_model()
            model_id = best_model["model_id"] if best_model else None
            add_prediction(
                user_id, record_id, model_id,
                result["prediction_result"], result["probability"],
                result["confidence_score"], result["risk_category"],
            )
            st.caption("✔ This assessment has been saved to your health history.")
        except Exception as e:
            st.warning(f"Prediction computed, but could not save to history: {e}")

        st.info(
            "👉 Visit **Risk Factor Analysis** to understand what's driving this result, "
            "or **Recommendations** for personalized next steps."
        )

    st.warning(
        "⚕️ **Disclaimer:** This prediction is generated by a machine learning model for "
        "screening and educational purposes only. It does not constitute a medical diagnosis. "
        "Please consult a qualified healthcare professional for clinical decisions."
    )
